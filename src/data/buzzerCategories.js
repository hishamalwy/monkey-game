export function getRandomBuzzerItem(categoryId, excludedIndices = []) {
  const cat = buzzerCategories.find(c => c.id === categoryId);
  if (!cat || cat.items.length === 0) return null;
  const available = cat.items
    .map((item, idx) => ({ item, idx }))
    .filter(({ idx }) => !excludedIndices.includes(idx));
  if (available.length === 0) {
    return { item: cat.items[Math.floor(Math.random() * cat.items.length)], idx: -1 };
  }
  const pick = available[Math.floor(Math.random() * available.length)];
  return { item: pick.item, idx: pick.idx };
}

export function getBuzzerCategoryById(id) {
  return buzzerCategories.find(c => c.id === id);
}
